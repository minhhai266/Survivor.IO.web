// =========================================
// LANGUAGE ENGINE
// =========================================

(function () {

    // Load saved language, default to "vi"
    let currentLang = localStorage.getItem("lang") || "vi";

    // Apply all translations to the page
    function applyTranslations(lang) {
        const dict = translations[lang];
        if (!dict) return;

        // Handle data-translate (used in index.jsp, footer.jsp, header.jsp)
        document.querySelectorAll("[data-translate]").forEach(el => {
            const key = el.getAttribute("data-translate");
            if (dict[key] !== undefined) {
                el.textContent = dict[key];
            }
        });

        // Handle data-key (used in characters.jsp, weapons.jsp, events.jsp)
        document.querySelectorAll("[data-key]").forEach(el => {
            const key = el.getAttribute("data-key");
            if (dict[key] !== undefined) {
                el.textContent = dict[key];
            }
        });

        // Update <html lang="..."> attribute
        document.documentElement.lang = lang;

        // Update toggle button label
        const langText = document.getElementById("langText");
        if (langText) {
            langText.textContent = lang.toUpperCase();
        }

        // Save preference
        localStorage.setItem("lang", lang);
        currentLang = lang;
    }

    // Toggle between vi <-> en
    function toggleLanguage() {
        const newLang = currentLang === "vi" ? "en" : "vi";
        applyTranslations(newLang);
    }

    // Bind toggle button
    function bindToggle() {
        const langToggle = document.getElementById("langToggle");
        if (langToggle) {
            langToggle.addEventListener("click", toggleLanguage);
        }
    }

    // Init on DOM ready
    if (document.readyState === "loading") {
        document.addEventListener("DOMContentLoaded", () => {
            applyTranslations(currentLang);
            bindToggle();
        });
    } else {
        applyTranslations(currentLang);
        bindToggle();
    }

})();